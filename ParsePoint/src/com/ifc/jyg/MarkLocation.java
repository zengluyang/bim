package com.ifc.jyg;

public class MarkLocation {

	private int a;
	private int b;
	private int c;
	
	public MarkLocation(int a, int b, int c) {
		this.setA(a);
		this.setB(b);
		this.setC(c);
	}

	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}

	public int getC() {
		return c;
	}

	public void setC(int c) {
		this.c = c;
	}
}
